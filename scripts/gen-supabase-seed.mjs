// Generate supabase/seed.sql (reference content) from the single source of
// truth, dolbomi-app/src/data/index.js. Run:  npm run gen:seed
//
// This keeps the Supabase reference tables and the SPA's offline fallback in
// lockstep (closes LOGIC-GAPS L2 — the data can no longer silently diverge).
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const data = await import(join(root, 'dolbomi-app/src/data/index.js'));

const q = (v) => (v == null ? 'null' : `'${String(v).replace(/'/g, "''")}'`);
const n = (v) => (v == null ? 'null' : Number(v));
const b = (v) => (v ? 'true' : 'false');
const j = (v) => (v == null ? 'null' : `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`);

const out = [];
out.push('-- DOLBOMI reference seed — GENERATED by scripts/gen-supabase-seed.mjs. Do not edit by hand.');
out.push('-- Run after 0001_init.sql. Safe to re-run (truncates reference tables first).');
out.push('');
out.push('truncate quest_pool, benefits, subquests, milestones, opportunities, titles, stat_defs restart identity cascade;');
out.push('');

// stat_defs
out.push('-- stat definitions (per-stat target + new-account starting value)');
data.stats.forEach((s, i) => {
  out.push(`insert into stat_defs (key, mil, en, real, tgt, base, ord) values (${q(s.key)}, ${q(s.mil)}, ${q(s.en)}, ${q(s.real)}, ${n(s.tgt)}, ${n(s.base)}, ${i});`);
});
out.push('');

// opportunities + milestones + subquests
out.push('-- opportunity catalog');
data.catalog.forEach((o, oi) => {
  out.push(`insert into opportunities (id, cat, stat, title, hot, sub, what, eligibility, apply_where, source, verified, cost, deadline, unlock_dday, started, reward, why, expected_pct, status, img, ord, tags) values (${q(o.id)}, ${q(o.cat)}, ${q(o.stat)}, ${q(o.title)}, ${b(o.hot)}, ${q(o.sub)}, ${q(o.what)}, ${q(o.eligibility)}, ${q(o.applyWhere)}, ${q(o.source)}, ${q(o.verified)}, ${q(o.cost)}, ${q(o.deadline)}, ${n(o.unlockDday)}, ${b(o.started)}, ${j(o.reward)}, ${q(o.why)}, ${n(o.expectedPct)}, ${q(o.status)}, ${q(o.img)}, ${oi}, ${j(o.tags || [])});`);
  o.milestones.forEach((m, mi) => {
    out.push(`insert into milestones (opp_id, id, title, date_label, ord) values (${q(o.id)}, ${q(m.id)}, ${q(m.title)}, ${q(m.date)}, ${mi});`);
    m.subquests.forEach((s, si) => {
      out.push(`insert into subquests (opp_id, id, milestone_id, text, size, xp, stat, default_done, service, ord) values (${q(o.id)}, ${q(s.id)}, ${q(m.id)}, ${q(s.text)}, ${q(s.size)}, ${n(s.xp)}, ${q(s.stat)}, ${b(s.done)}, ${j(s.service)}, ${si});`);
    });
  });
});
out.push('');

// benefits
out.push('-- benefits');
data.benefits.forEach((bf, i) => {
  out.push(`insert into benefits (id, title, icon, tone, value, where_, branches, opp_id, headline, ord) values (${q(bf.id)}, ${q(bf.title)}, ${q(bf.icon)}, ${q(bf.tone)}, ${q(bf.value)}, ${q(bf.where)}, ${j(bf.branches || ['전군'])}, ${q(bf.oppId)}, ${b(bf.headline)}, ${i});`);
});
out.push('');

// titles
out.push('-- titles (req drives automatic awarding)');
data.titles.forEach((t, i) => {
  out.push(`insert into titles (name, descr, rarity, legendary, req, ord) values (${q(t.name)}, ${q(t.desc)}, ${q(t.rarity)}, ${b(t.legendary)}, ${j(t.req)}, ${i});`);
});
out.push('');

// quest pool
out.push('-- quest pool (check-in regenerates "오늘 밤의 3" from these by energy)');
data.questPool.forEach((p) => {
  out.push(`insert into quest_pool (stat, txt, min, xp, hard, tags) values (${q(p.stat)}, ${q(p.txt)}, ${n(p.min)}, ${n(p.xp)}, ${b(p.hard)}, ${j(p.tags || [])});`);
});
out.push('');

writeFileSync(join(root, 'supabase/seed.sql'), out.join('\n'));
console.log(`✓ wrote supabase/seed.sql (${out.length} lines)`);
